
package ReportesDTOs;

public class CalificacionPromedio {
    
    private String titulo;
    private double promedio_estrellas;

    public CalificacionPromedio() {
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public double getPromedio_estrellas() {
        return promedio_estrellas;
    }

    public void setPromedio_estrellas(double promedio_estrellas) {
        this.promedio_estrellas = promedio_estrellas;
    }
    
    
    
}
