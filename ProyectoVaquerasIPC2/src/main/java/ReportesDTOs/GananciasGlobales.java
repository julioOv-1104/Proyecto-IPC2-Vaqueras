
package ReportesDTOs;

public class GananciasGlobales {
    
    private double total_ingresado, comision_plataforma, ganancia_total_empresas;

    public GananciasGlobales() {
    }

    public double getTotal_ingresado() {
        return total_ingresado;
    }

    public void setTotal_ingresado(double total_ingresado) {
        this.total_ingresado = total_ingresado;
    }

    public double getComision_plataforma() {
        return comision_plataforma;
    }

    public void setComision_plataforma(double comision_plataforma) {
        this.comision_plataforma = comision_plataforma;
    }

    public double getGanancia_total_empresas() {
        return ganancia_total_empresas;
    }

    public void setGanancia_total_empresas(double ganancia_total_empresas) {
        this.ganancia_total_empresas = ganancia_total_empresas;
    }
    
    
    
}
