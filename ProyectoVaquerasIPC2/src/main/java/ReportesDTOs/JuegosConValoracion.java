
package ReportesDTOs;

public class JuegosConValoracion {
    
    private String titulo;
    private double promedio_comunidad, mi_valoracion;

    public JuegosConValoracion() {
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public double getPromedio_comunidad() {
        return promedio_comunidad;
    }

    public void setPromedio_comunidad(double promedio_comunidad) {
        this.promedio_comunidad = promedio_comunidad;
    }

    public double getMi_valoracion() {
        return mi_valoracion;
    }

    public void setMi_valoracion(double mi_valoracion) {
        this.mi_valoracion = mi_valoracion;
    }
    
    
    
}
